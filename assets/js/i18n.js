let currentLang = localStorage.getItem('fids_lang') || 'id';

function setLanguage(lang) {
    if (I18N_DICT[lang]) {
        currentLang = lang;
        localStorage.setItem('fids_lang', lang);
        applyTranslations();
        
        // Dispatch custom event so main.js can update dynamic content
        window.dispatchEvent(new Event('languageChanged'));
    }
}

function applyTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (I18N_DICT[currentLang] && I18N_DICT[currentLang][key]) {
            // Apply the split-flap animation class
            el.classList.remove('flap-animate');
            void el.offsetWidth; // Trigger reflow to restart animation
            el.classList.add('flap-animate');
            
            // Swap the text exactly when the text is faded out
            setTimeout(() => {
                if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                    el.placeholder = I18N_DICT[currentLang][key];
                } else if (el.tagName === 'BUTTON' && el.innerHTML.includes('<i class=')) {
                    el.innerHTML = el.innerHTML.replace(/(<i class="[^"]+"><\/i>\s*)(.*)/i, '$1' + I18N_DICT[currentLang][key]);
                } else {
                    el.innerHTML = I18N_DICT[currentLang][key];
                }
            }, 400); // ~halfway through the 0.8s animation
        }
    });

    // Update active state on language switcher buttons if they exist
    const btnID = document.getElementById('lang-id');
    const btnEN = document.getElementById('lang-en');
    if (btnID && btnEN) {
        if (currentLang === 'id') {
            btnID.classList.add('active', 'text-gold');
            btnID.classList.remove('text-white-50');
            btnEN.classList.remove('active', 'text-gold');
            btnEN.classList.add('text-white-50');
        } else {
            btnEN.classList.add('active', 'text-gold');
            btnEN.classList.remove('text-white-50');
            btnID.classList.remove('active', 'text-gold');
            btnID.classList.add('text-white-50');
        }
    }
}

// Function helper to get translated string by key for use in JS
function __t(key) {
    if (I18N_DICT[currentLang] && I18N_DICT[currentLang][key]) {
        return I18N_DICT[currentLang][key];
    }
    return key; // Fallback
}

// Run translation on load
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
    initTheme();
});

/* ============================================
   THEME TOGGLE LOGIC (DARK/LIGHT)
   ============================================ */
// Deteksi tema: 1) Cek localStorage (pilihan manual user), 2) Ikuti sistem device
function getSystemTheme() {
    if (window.matchMedia && window.matchMedia('(prefers-color-scheme: light)').matches) {
        return 'light';
    }
    return 'dark';
}
let currentTheme = localStorage.getItem('fids_theme') || getSystemTheme();

function initTheme() {
    document.documentElement.setAttribute('data-theme', currentTheme);
    updateThemeIcon();
    
    // Pantau perubahan tema sistem secara real-time (jika user belum pernah set manual)
    window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e) => {
        if (!localStorage.getItem('fids_theme')) {
            executeThemeChange(e.matches ? 'dark' : 'light');
        }
    });
}

function toggleTheme(event) {
    const isDark = currentTheme === 'dark';
    const nextTheme = isDark ? 'light' : 'dark';
    
    // Fallback for browsers that do not support View Transitions API
    if (!document.startViewTransition) {
        executeThemeChange(nextTheme);
        return;
    }
    
    // Get click coordinates for the ripple center (fallback to center of screen)
    const x = event?.clientX ?? window.innerWidth / 2;
    const y = event?.clientY ?? window.innerHeight / 2;
    
    // Calculate the maximum radius required to cover the whole screen
    const endRadius = Math.hypot(
        Math.max(x, window.innerWidth - x),
        Math.max(y, window.innerHeight - y)
    );
    
    // Add a class to disable normal CSS color transitions during the view transition
    document.documentElement.classList.add('theme-transitioning');
    
    const transition = document.startViewTransition(() => {
        executeThemeChange(nextTheme);
    });
    
    transition.ready.then(() => {
        const clipPath = [
            `circle(0px at ${x}px ${y}px)`,
            `circle(${endRadius}px at ${x}px ${y}px)`
        ];
        
        document.documentElement.animate(
            {
                clipPath: isDark ? clipPath : [...clipPath].reverse(),
            },
            {
                duration: 600,
                easing: 'cubic-bezier(0.25, 1, 0.5, 1)',
                pseudoElement: isDark ? '::view-transition-new(root)' : '::view-transition-old(root)',
            }
        );
    });
    
    transition.finished.finally(() => {
        document.documentElement.classList.remove('theme-transitioning');
    });
}

function executeThemeChange(theme) {
    currentTheme = theme;
    document.documentElement.setAttribute('data-theme', currentTheme);
    localStorage.setItem('fids_theme', currentTheme);
    updateThemeIcon();
}

function updateThemeIcon() {
    const btn = document.getElementById('theme-toggle');
    if (btn) {
        if (currentTheme === 'light') {
            btn.innerHTML = '<i class="fa-solid fa-moon"></i>';
        } else {
            btn.innerHTML = '<i class="fa-solid fa-sun"></i>';
        }
    }
}
